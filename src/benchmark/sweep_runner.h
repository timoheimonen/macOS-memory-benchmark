/**
 * @file sweep_runner.h
 * @brief Multi-configuration benchmark sweep runner.
 */

#ifndef SWEEP_RUNNER_H
#define SWEEP_RUNNER_H

#include <cstddef>
#include <cstdlib>

struct BenchmarkConfig;

/**
 * @brief Calculate the number of concrete runs generated by sweep specs.
 * @param config Parsed benchmark configuration with sweep specs.
 * @return Cartesian product size, or max size_t on overflow.
 */
size_t calculate_sweep_run_count(const BenchmarkConfig& config);

/**
 * @brief Execute a parameter sweep and write the combined JSON output.
 * @param base_config Parsed and validated base configuration.
 * @return EXIT_SUCCESS on success, EXIT_FAILURE on error.
 */
int run_sweep_mode(const BenchmarkConfig& base_config);

#endif  // SWEEP_RUNNER_H
